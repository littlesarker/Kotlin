fun main(args: Array<String>) {

    //variables are containers for data values
    //in kotlin variable can be (var)/(val)

    var name = "Rahim"
    var rahim =24

    println(name)
    println(rahim)

    //The difference between var and val is
   // that variables declared with the var keyword can be changed/modified, while val variables cannot.
  //in Kotlin do not need to be declared with a specified type .But if you want you can do that.

    var Uvnam :String="GUB"
    val age :Int =22

    println(Uvnam)
    println(age)


    //without specify the type of variable .its can"t be un assigned variable


}